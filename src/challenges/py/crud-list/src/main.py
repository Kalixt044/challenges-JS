letters = ['A', 'B', 'C', 'D', 'E', 'F']

# 1. Agregar la letra G al final de la lista
letters.append('G')

# 2. Reemplazar la letra en la posición 0 con la letra Z
letters[0] = 'Z'

# 3. Eliminar la letra C de la lista
letters.remove('C')

# 4. Imprimir la lista resultante al revés
print(letters[::-1])
